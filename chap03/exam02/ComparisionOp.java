package exam02;

public class ComparisionOp { // 비교 (관계) 연산자
	public static void main(String[] args) {
		
		System.out.println(350 < 200);
		System.out.println(350 > 200);
		System.out.println(350 <= 200);
		System.out.println(350 >= 200);
		System.out.println(350 == 200);
		System.out.println(350 != 200); // 왼쪽과 오른쪽 값이 다르냐?
		
		
		
	}
}
